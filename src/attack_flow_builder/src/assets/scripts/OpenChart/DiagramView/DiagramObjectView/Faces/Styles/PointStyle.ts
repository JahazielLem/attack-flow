/**
 * Point style definition.
 */
export type PointStyle = {

    /**
     * The point's radius.
     */
    radius: number;

    /**
     * The point's fill color.
     */
    fillColor: string;

    /**
     * The point's stroke color.
     */
    strokeColor: string;

    /**
     * The point's stroke width.
     */
    strokeWidth: number;

};
