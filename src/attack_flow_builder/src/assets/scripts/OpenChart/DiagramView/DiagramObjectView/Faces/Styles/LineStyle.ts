/**
 * Line style definition.
 */
export type LineStyle = {

    /**
     * The line's width.
     */
    width: number;

    /**
     * The line's hitbox with.
     */
    hitboxWidth: number;

    /**
     * The line's border radius.
     */
    borderRadius: number;

    /**
     * The line's cap size.
     */
    capSize: number;

    /**
     * The line's color.
     */
    color: string;

    /**
     * The line's color when selected.
     */
    selectColor: string;

};
